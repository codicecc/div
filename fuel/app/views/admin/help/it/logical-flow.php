            <h2>Flusso Logico</h2>
            <p>Per arrivare alla creazione di un ordine è necessario:
				<ul>
					<li>Aver caricato le misure degli studenti:</li>
					<ul>
						<li>Caricamento è automatico, caricando un file CSV opportunamente formattato. Questa procedura crea, automaticamente, anche gli studenti;</li>
						<li>Caricamento manuale associando le misure allo studente;</li>
						<li>Gli studenti sono raggruppati per scuola;</li>
						<li>Le misure fanno riferimento a parti del corpo, per esempio: spalle, bacino, etc.</li>
					</ul>
					
					<li>Identificare la taglia dell’allievo:</li>
					<ul>
						<li>Caricare i range (intervalli) di valori per identificare una taglia (in genere si fa una volta);</li>
						<li>Il calcolo della taglia è fatto in automatico, è possibile calcolarla su più parti del corpo, contemporaneamente, per esempio una taglia 2’ per le spalle e 3’ per il bacino;</li>
						<li>Ciò permette di fornire documentazione adeguata alla sarta;</li>
					</ul>

					<li>Avere dei modelli a catalogo. I modelli sono costituiti da:</li>
					<ul>
						<li>Elementi, per esempio: body, tutù, etc.</li>
						<li>Dettagli degli elementi, per esempio: top, bretelline, etc.</li>
						<li>Ogni dettaglio ha un nota generica, classificata come attributo, per esempio: un body costituito da un top con attributo lycra.</li>
					</ul>
					<li>Solo a questo punto è possibile creare un ordine compilando i seguenti campi:</li>
					<ul>
						<li>Nome generico;</li>
						<li>Selezione del modello;</li>
						<li>Selezione della scuola;</li>
						<li>Eventuali note;</li>
						<li>Indicazione dello stato dell’ordine: aperto (in corso), chiuso;</li>
						<li>Selezione degli studenti interessati.</li>
					</ul>
				</ul>
			</p>
          </div>			
